            <h5>
            <p style="color:black"><strong><u>NOTE: </u></strong><u>While we are keeping majority of the rules the same as ESL, we made slight modifications to our rulebook and we encourage everyone to read them thoroughly. Failure to comply with the rules of the tournament due to any individual&rsquo;s lack of interest to read/understand them will result in their own loss and the organizers won&rsquo;t be held responsible.</u></p>
            <br><br>
<ol>
<li style="color:black"><strong> R6:Siege Tournament Rules</strong></li>
</ol>
<p style="color:black"><h2><strong>1.1. Players</strong></h2></p>
<p style="color:black"><h4><strong>1.1.1. Eligibility</strong></h4></p>
<p style="color:black">Any player that has an original r6s account is eligible to participate in the tournament. However, a player can play for one team only, he/she will not be allowed to play for any other team except for the one which he/she is registered under.</p>
<h3>1.1.2. Requirements</h3>
<p style="color:black">All players need to enter their correct game account associated to the PC platform in the registration page before the start of the tournament.</p>
<h3>1.1.3. Technical Issues</h3>
<p style="color:black">Players are responsible for their own hardware and internet connection for all matches until quarter finals. Matches will not be rescheduled because of technical issues or substitute a player. If a team can&acute;t play with the number of players which are written in the requirements then this will be resulted as forfeit.</p>
<h4><strong>1.1.3.1. Connection</strong></h4>
<p style="color:black">Everyone is responsible to have a connection (ping) as good as possible for their team region and technical situation. Also, all downloads and other programs which are unnecessary for the match must be turned off. For abusing this rule, the team can be disqualified.</p>
<h4><strong>1.1.3.2. High Ping</strong></h4>
<p style="color:black">The ping limit for every match is 150 ms. If a player is over this value a re-host will be attempted. In case the player still has a ping over 150 ms, he/she will have to play the match regardless of what ping he/she is getting. However, if a situation arises where all the members of a team are experiencing high ping, then the tournament organizers will take necessary steps to ensure fair play. It only counts as high ping if the ping is continuously over 150ms (same player)!</p>
<p style="color:black">&nbsp;</p>
<h3>&nbsp;</h3>
<h3>1.1.4. Forbidden controllers</h3>
<p style="color:black">Usage of a non-licensed controller is strictly prohibited. Usage of all console keyboard adapters is strictly prohibited. If a licensed controller has a macro/turbo/auto-fire option, it cannot be used during the tournament, otherwise the match will be considered as a forfeit by the transgressing player or team.</p>
<h3>1.1.5. Player Settings</h3>
<h4><strong>1.1.5.1. Graphics drivers, or similar tools</strong></h4>
<p style="color:black">Any modification or changing of the game using external graphics solutions or other 3rd party programs are strictly prohibited and may be punished under the cheating paragraph.</p>
<p style="color:black">Furthermore, it's forbidden to use all kinds of overlays which will show the usage rate of the system in any way in-game (e.g. Nvidia SLI display, Rivatuner Overlay). Overlays which will show only the frames per second (FPS) are not forbidden and can be used.</p>
<h4><strong>1.1.5.2. Custom Data</strong></h4>
<p style="color:black">Any changes to sprites, skins, score boards, crosshairs are strictly disallowed, also only the official models are allowed. If a player plays with custom files (not models) or removed files that have been installed by the game, then this will result in disqualification of the player.</p>
<h2>1.2. Team rules</h2>
<h3>1.2.1. Roster</h3>
<p style="color:black">Team rosters must consist of at least 5 players to participate in a tournament. All roster changes must be made prior the generation of the matches.</p>
<h3>1.2.2. Substitute Players</h3>
<p style="color:black">It is only allowed to substitute a player before the match starts. If the match is started (picking operator and spawn points) all 5 players of a team must play the game till the end. It is possible to switch players in a Bo3 between the maps.</p>
<h2>1.3. Tournament rules</h2>
<h3>1.3.1. Settings for the tournament</h3>
<p style="color:black">All players are expected to follow the given settings. Different agreements are not allowed.</p>
<h3>1.3.2. Map Veto and Hosting rights</h3>
<p style="color:black">Map bans must be done via the map veto system on the matchpage.</p>
<p style="color:black">All the matches until the quarter finals will be hosted by the organizers.</p>
<p style="color:black">The Team who picked the Map in a Best of One cannot choose the starting sides; the other Team can do that. During a Best of Three each can pick the sides of the Map picked by the other Team. Team A picks the Map and Team B can choose the starting sides on that one. Team B picks a map as well and Team A can choose the starting sides on this one.</p>
<h3>1.3.3. Start of the matches</h3>
<p style="color:black">All the matches will be started according to the schedule fixed by the organizers. The organizers hold the right to change the schedule in case they deem it necessary. All teams and participants must adhere to the schedule strictly. Failure to do so may result in disqualification.</p>
<h3>1.3.4. No show</h3>
<p style="color:black">If a contestant is not ready to play after 15 minutes, then they should be reported as a no show to an organizer. The countdown starts from the moment both teams have progressed to the match, and not at the time stated in the match page.</p>
<h3>1.3.5. Forfeit</h3>
<p style="color:black">If a contestant chooses to forfeit a match, they will forfeit any prizes and if possible, will be replaced by their opponent in the previous round.</p>
<h3>1.3.6. Protests</h3>
<p style="color:black">If a controversial situation occurs, whether or not it is described in this rulebook during a match, the team that has spotted it should immediately stop the match and inform the organizing host about it. If the team does not stop the match and continues to play, it will be taken as an acceptance of the situation. Because of that the team will lose all rights to file a protest concerning that situation after the game has ended. Protests must be made immediately, or they may be ignored depending on the circumstances.</p>
<h3>1.3.7. Results</h3>
<p style="color:black">The results of the match will be recorded by the organizing host. In case any change is necessary in the results, players are requested to inform the host immediately after the match ends. The host will determine if the requested change is legitimate or not.</p>
<h3>1.3.8. Disqualification</h3>
<ul>
<li style="color:black">The match must be played as soon as possible (both teams added to the match).</li>
<li style="color:black">To keep the delay during the tournament as low as possible, we reserve the right to disqualify teams from the tournament. This will only be done in cases where a team shows no real effort to get a match done or is even obviously blocking its completion. In severe cases, this can even affect both teams.</li>
</ul>
<h2>1.4. Ingame</h2>
<h3>1.4.1. Player drops</h3>
<p style="color:black">If a player drops from the server during a match, the round will continue uninterrupted until the end (Round starts with the first second of the preparation phase). After completion of the round, the player who disconnected will be allowed to re-join the server or a re-host can be requested. The match is considered &ldquo;live&rdquo; when the first round has started. At least half of all players need to remain and the match needs to be played until the match end and a winner is decided. If this isn&rsquo;t possible due to connection issues, it will be considered as a forfeit in favor for the team who is able to have all players on the server.</p>
<p style="color:black">Every team can request a re-host of the server once in a match. If problems like this appear again, such as a player dropping out when the match has already been re-hosted, the team needs to play the match with the remaining players to its conclusion. Any abuse of this rule will be considered as deception and will result in penalization including disqualification of the team.</p>
<p style="color:black">If a team leaves the server without informing the opponent, the team risks forfeiting the match. Each user is responsible for his/her own hardware.</p>
<h3>1.4.2. Re-host</h3>
<p style="color:black">In a re-hosted game, each player needs to choose the same settings as they picked at the start of the original game. Only hosts and organizers can decide, if the game will be completely restarted by a score 0:0.</p>
<h3>1.4.3. Pausing the game</h3>
<p style="color:black">If a player requests a pause in a game, they must state the reason for the interruption and how long it will approximately take. The request will be granted as seen fit by the organizing host. The game will only be resumed after all players are ready. The pause should not be longer than 10 minutes.</p>
<p style="color:black">Violation of this rule are handled as unsportsmanlike behavior and can lead to a re-game.</p>
<h3>1.4.4. Illegal Actions</h3>
<p style="color:black">Any actions that result in an unfair advantage are illegal. This includes bug and glitch using of any kind. In a tournament by using any bug or glitch, if a team got a warning, by the second time of using it, the team will be disqualified.</p>
<h4><strong>1.4.4.1. Spawnkilling / Spawn peaking</strong></h4>
<ul>
<li style="color:black">Killing somebody who already spawned, moved out of cover or is spawn rushed is considered spawn peaking and will not be rated as glitching / bug using.</li>
</ul>
<h3>1.5. Personal Streaming</h3>
<p style="color:black">Personal streaming is allowed. However, if the streaming causes any issues on the streamer&rsquo;s side (e.g. high ping, glitch, rubber-banding etc.), the organizer/host will not bear any responsibility and the match will continue as is.</p>
<h2>1.6. Cheating</h2>
<p style="color:black">If any player is found to be cheating or using any mods/hacks, the organizers reserve the right to ban the player and the whole team. Moreover, if any player notices any questionable gameplay from the opposition team, they are requested to inform the organizing host immediately. The organizing host will decide the repercussion as they deem necessary.</p>
